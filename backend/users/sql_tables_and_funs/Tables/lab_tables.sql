CREATE TABLE IF NOT EXISTS public.lab_test_orders
(
    order_id               uuid NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id             uuid NOT NULL,
    lab_id                 uuid NOT NULL,
    doctor_id              uuid,
    appointment_id         integer,
    order_status           character varying(20) NOT NULL DEFAULT 'PLACED',     -- 'PLACED', 'ACCEPTED', 'IN_PROGRESS', 'COMPLETED', 'CANCELLED'
    payment_status         character varying(20) NOT NULL DEFAULT 'PENDING',    -- 'PENDING', 'PAID', 'FAILED', 'REFUNDED'
    sample_collection_status character varying(20) NOT NULL DEFAULT 'PENDING',  -- 'PENDING', 'COLLECTED', 'REJECTED'
    price_at_order   numeric(10,2) NOT NULL,                            -- price_at_order >= 0
    discount_amount  numeric(10,2) NOT NULL DEFAULT 0,  
    total_amount           numeric(10,2) NOT NULL DEFAULT 0,                    -- total_amount >= 0
    notes                  text,
    ordered_at             timestamp with time zone NOT NULL DEFAULT now(),
    updated_at             timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.lab_test_results
(
    result_id        integer NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id    uuid        NOT NULL REFERENCES lab_test_orders(order_id) ON DELETE CASCADE,
    parameter_id integer    NOT NULL REFERENCES test_parameters(parameter_id),
    result_value     text NOT NULL,
    result_flag      character varying(20),
    result_notes     text,
    reported_by_id   uuid,
    created_at       timestamp with time zone NOT NULL DEFAULT now(),
    updated_at       timestamp with time zone NOT NULL DEFAULT now()
    CONSTRAINT uq_result_per_order UNIQUE (order_id, parameter_id)
);